package com.example.FingerTips.HelperClass;

public class Topic {
	
	private int topic_id;
	private String topic_name;
	private String topic_data;
	private int la_topic_stat;

	public int getTopic_id() {
		return topic_id;
	}

	public void setTopic_id(int topic_id) {
		this.topic_id = topic_id;
	}

	public String getTopic_name() {
		return topic_name;
	}

	public void setTopic_name(String topic_name) {
		this.topic_name = topic_name;
	}

	public String getTopic_data() {
		return topic_data;
	}

	public void setTopic_data(String topic_data) {
		this.topic_data = topic_data;
	}

	public int getLa_topic_stat() {
		return la_topic_stat;
	}

	public void setLa_topic_stat(int la_topic_stat) {
		this.la_topic_stat = la_topic_stat;
	}

}
